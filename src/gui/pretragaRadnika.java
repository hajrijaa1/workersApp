    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
         String pretraga = jTextField1.getText();
        
        ResultSet rezultat=null;   
        try {
            Konekcija konekcija=new Konekcija();
            rezultat=konekcija.pretragaRadnika(pretraga);
            while(rezultat.next())
            {
                String ime=rezultat.getString("ime");
                String prezime=rezultat.getString("prezime");
                String godinaRodjenja=rezultat.getString("godinaRodjenja");
                int godina;
                godina = Integer.parseInt(godinaRodjenja);
                String naziv = rezultat.getString("naziv");
                String podaci[]={ime, prezime, godinaRodjenja, naziv};
                DefaultTableModel tabela=(DefaultTableModel)jTable1.getModel();
                tabela.addRow(podaci);
                
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

