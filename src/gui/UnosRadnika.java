    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ime = jTextField1.getText();
        String prezime = jTextField2.getText();
        String radnik=jComboBox1.getSelectedItem().toString();
        int godina;
        try{
            godina=Integer.parseInt(radnik);
        }catch(NumberFormatException e){
            godina=0;
        }
        lblPoruka.setText("Uspje≈°no ste unijeli radnika u bazu podataka!");
        lblPoruka.setForeground(new Color(84, 140, 47));
        try {
            this.konekcija.unosRadnika(ime, prezime, godina);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

